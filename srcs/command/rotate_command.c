#include "../../includes/push_swap.h"

void	rotate(t_stack **stack)
{
	t_stack	*temp;
	t_stack	*first;

	if (*stack == NULL || (*stack)->next == NULL)
		return ;
	first = *stack;
	temp = *stack;
	while (temp->next != NULL)
		temp = temp->next;
	*stack = (*stack)->next;
	temp->next = first;
	first->next = NULL;
}

void	ra(t_stack **stack_a)
{
	rotate(stack_a);
	ft_putstr("ra\n");
}

void	rb(t_stack **stack_b)
{
	rotate(stack_b);
	ft_putstr("rb\n");
}

void	rr(t_stack **stack_a, t_stack **stack_b)
{
	rotate(stack_a);
	rotate(stack_b);
	ft_putstr("rr\n");
}